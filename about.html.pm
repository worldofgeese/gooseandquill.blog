◊(define-meta title "About Goose and Quill")

◊emph{Goose and Quill} is Tao's writing nook on the internet.

The site is written with ◊link["//pollenpub.com"]{Pollen}, which means it’s basically a LISP program that spits out a website and a book from the same source◊numbered-note{Joel Dueck has written about this dual-publishing approach in the article ◊link["https://thelocalyarn.com/excursus/secretary/posts/web-books.html"]{The Unbearable Lightness of Web Pages}.}. You can see ◊link["https://github.com/worldofgeese/gooseandquill.blog"]{the source code on GitHub}.

The fonts used on this site are ◊link["http://adobe-fonts.github.io/source-sans-pro/"]{Source Sans Pro} (free) for headings and body, and ◊link["http://practicaltypography.com/triplicate.html"]{Triplicate} for code.
